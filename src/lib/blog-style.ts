/**
 * 본문 텍스트의 브랜드 표기를 공식 형태인 "플라트 라이프" (공백 포함) 로 정규화.
 *  1) "플라트라이프" → "플라트 라이프"   (붙여쓰기 → 띄어쓰기)
 *  2) "플라트" (단독) → "플라트 라이프"   (이미 '라이프'/'(공백)라이프' 가 따라오는 경우는 보존)
 *
 * Writer 출력 후처리 + 검수 재작성 후처리 양쪽에서 호출됨.
 */
export function normalizeBrandName(text: string): string {
  if (!text) return ""
  let s = text.replace(/플라트라이프/g, "플라트 라이프")
  s = s.replace(/플라트(?![ ]?라이프)/g, "플라트 라이프")
  return s
}

