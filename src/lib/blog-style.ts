/** 프롬프트 블록으로 직렬화 */
export function styleGuideForPrompt(opts?: { withImageSlots?: boolean }): string {
  const s = PLOTT_BLOG_STYLE
  const imgBlock = opts?.withImageSlots
    ? `\n\n## 이미지 배치 규칙\n${s.imageSlots.map((x) => `- ${x}`).join("\n")}`
    : ""

  return `[플라트라이프 블로그 스타일 가이드]

## Voice
${s.voice}

## 구조 (이 순서대로 H2 구성)
${s.structure.map((x, i) => `${i + 1}. ${x}`).join("\n")}

## Heading
- H1: ${s.headingStyle.h1}
- H2: ${s.headingStyle.h2}
- H3: ${s.headingStyle.h3}

## Rhetoric
${s.rhetoric.map((x) => `- ${x}`).join("\n")}

## 길이
- 전체: ${s.length.total}
- H2 섹션당: ${s.length.sectionTarget}
- 도입부: ${s.length.introTarget}

## ⚠️ 필수 준수 (위반 시 재작성)
${s.hardConstraints.map((x, i) => `${i + 1}. ${x}`).join("\n")}

## 금지어 / 대체어
${s.bannedPhrases.map((x) => `- ❌ "${x.avoid}" → ✅ "${x.use}"`).join("\n")}

## CTA 패턴
${s.ctaPattern.map((x) => `- ${x}`).join("\n")}

## 참고 발췌 (이 문체·리듬을 그대로 따라 하세요)
${s.sampleExcerpt}${imgBlock}
`
}
